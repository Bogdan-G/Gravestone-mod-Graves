package gravestone.models.block.graves;

import net.minecraftforge.fml.relauncher.Side;
import net.minecraftforge.fml.relauncher.SideOnly;
import gravestone.models.block.ModelGraveStone;
import net.minecraft.client.model.ModelRenderer;
import org.lwjgl.opengl.GL11;

/**
 * GraveStone mod
 *
 * @author NightKosh
 * @license Lesser GNU Public License v3 (http://www.gnu.org/licenses/lgpl.html)
 */
@SideOnly(Side.CLIENT)
public class ModelDogStatueGraveStone extends ModelGraveStone {

    private ModelRenderer head;
    private ModelRenderer body;
    private ModelRenderer leg1;
    private ModelRenderer leg2;
    private ModelRenderer leg3;
    private ModelRenderer leg4;
    private ModelRenderer mane;
    private ModelRenderer tail;

    public ModelDogStatueGraveStone() {
        float f = 0;
        float f1 = 13.5F;
        this.head = new ModelRenderer(this, 0, 0);
        this.head.addBox(-3, -3, -2, 6, 6, 4, f);
        this.head.setRotationPoint(-1, f1, -7);
        this.body = new ModelRenderer(this, 18, 14);
        this.body.addBox(-4, -2, -3, 6, 9, 6, f);
        this.body.setRotationPoint(0, 14, 2);
        this.mane = new ModelRenderer(this, 21, 0);
        this.mane.addBox(-4, -3, -3, 8, 6, 7, f);
        this.mane.setRotationPoint(-1, 14, 2);
        this.leg1 = new ModelRenderer(this, 0, 18);
        this.leg1.addBox(-1, 0, -1, 2, 8, 2, f);
        this.leg1.setRotationPoint(-2.5F, 16, 7);
        this.leg2 = new ModelRenderer(this, 0, 18);
        this.leg2.addBox(-1, 0, -1, 2, 8, 2, f);
        this.leg2.setRotationPoint(0.5F, 16, 7);
        this.leg3 = new ModelRenderer(this, 0, 18);
        this.leg3.addBox(-1, 0, -1, 2, 8, 2, f);
        this.leg3.setRotationPoint(-2.5F, 16, -4);
        this.leg4 = new ModelRenderer(this, 0, 18);
        this.leg4.addBox(-1, 0, -1, 2, 8, 2, f);
        this.leg4.setRotationPoint(0.5F, 16, -4);
        this.tail = new ModelRenderer(this, 9, 18);
        this.tail.addBox(-1, 0, -1, 2, 8, 2, f);
        this.tail.setRotationPoint(-1, 12, 8);
        this.head.setTextureOffset(16, 14).addBox(-3, -5, 0, 2, 2, 1, f);
        this.head.setTextureOffset(16, 14).addBox(1, -5, 0, 2, 2, 1, f);
        this.head.setTextureOffset(0, 10).addBox(-1.5F, 0, -5, 3, 3, 4, f);
    }

    /**
     * Sets the model's various rotation angles. For bipeds, par1 and par2 are
     * used for animating the movement of arms and legs, where par1 represents
     * the time(so that arms and legs swing back and forth) and par2 represents
     * how "far" arms and legs can swing at most.
     */
    public void setRotationAngles(float par2, float par3, float par4, float par5) {
        this.head.rotateAngleX = -0.08F;
        this.mane.setRotationPoint(-1, 16, -3);
        this.mane.rotateAngleX = ((float) Math.PI * 2 / 5F);
        this.mane.rotateAngleY = 0;
        this.body.setRotationPoint(0, 18, 0);
        this.body.rotateAngleX = ((float) Math.PI / 4F);
        this.leg1.setRotationPoint(-2.5F, 22, 2);
        this.leg1.rotateAngleX = ((float) Math.PI * 3 / 2F);
        this.leg2.setRotationPoint(0.5F, 22, 2);
        this.leg2.rotateAngleX = ((float) Math.PI * 3 / 2F);
        this.leg3.rotateAngleX = 5.811947F;
        this.leg3.setRotationPoint(-2.49F, 17, -4);
        this.leg4.rotateAngleX = 5.811947F;
        this.leg4.setRotationPoint(0.51F, 17, -4);
        this.tail.setRotationPoint(-1, 21, 6);
        this.tail.rotateAngleX = 1.7278761F;
    }

    @Override
    public void renderAll() {
        this.setRotationAngles(0.0625F, 0.0625F, 0.0625F, 0.0625F);
        float par7 = 0.0625F;
        GL11.glPushMatrix();
        GL11.glTranslatef(0, 5 * par7, 2 * par7);
        this.head.renderWithRotation(par7);
        GL11.glPopMatrix();
        GL11.glPushMatrix();
        GL11.glScalef(0.5F, 0.5F, 0.5F);
        GL11.glTranslatef(0, 24 * par7, 0);
        this.body.render(par7);
        this.leg1.render(par7);
        this.leg2.render(par7);
        this.leg3.render(par7);
        this.leg4.render(par7);
        this.mane.render(par7);
        this.tail.render(par7);
        GL11.glPopMatrix();
    }
}
